import matplotlib.pyplot as plt
from spatialmath import SE3
import numpy as np

# =====================================================================
# 1. 初始化 3D 仿真画布 (Spatialmath 依赖 Matplotlib 绘图)
# =====================================================================
fig = plt.figure()
ax = fig.add_subplot(111, projection='3d')
# 设定 3D 画布的显示边界 [Xmin, Xmax, Ymin, Ymax, Zmin, Zmax]
ax.set_xlim([-1, 4])
ax.set_ylim([-1, 4])
ax.set_zlim([0, 4])

# 先在原点画出静止不动的 【世界坐标系 {W}】
SE3().plot(frame='W', color='black', dims=[-1, 4, -1, 4, 0, 4], ax=ax)

# =====================================================================
# 2. 用 Spatialmath 步步追踪无人机的姿态 (SE3)
# =====================================================================

# 【步骤 1】: 无人机垂直起飞到 z = 3.0 米的地方
# SE3.Tz(3) 自动生成一个 [旋转为0, 平移为(0,0,3)] 的 4x4 齐次矩阵
T_takeoff = SE3.Tz(3.0)

# 【步骤 2】: 无人机在空中原地偏航左转 45 度
# SE3.Rz(45, 'deg') 自动生成一个 [绕Z轴旋转45度, 平移为0] 的 4x4 矩阵
T_yaw = SE3.Rz(45.0, unit='deg')

# 【步骤 3】: 无人机为了拍摄地面，低头（俯仰）15 度
T_pitch = SE3.Ry(15.0, unit='deg')

# =====================================================================
# 3. 终极合体：由于是在自身局部坐标系下连续运动，矩阵从左往右依次相乘
# =====================================================================
T_drone = T_takeoff * T_yaw * T_pitch

print("--- 无人机最终状态的 $4\times4$ 齐次变换矩阵 (SE3) ---")
print(T_drone)

# =====================================================================
# 4. 一键可视化：把无人机此时的 3D 坐标系画在刚才的画布上
# =====================================================================
# plot() 会自动遵循机器人界的通用国际惯例：
# X轴画成【红线】(Red) -> 代表车头朝向
# Y轴画成【绿线】(Green) -> 代表左侧机翼
# Z轴画成【蓝线】(Blue) -> 代表机身垂直上方
# (简称 RGB 对应 XYZ)
T_drone.plot(frame='Drone', color='blue', ax=ax)

# 展示 3D 图形界面
plt.show()